package arraystudy;

public class Multidimentionalarray 
{

	public static void main(String[] args)
	{
	char C[][]= {{'A','B'},{'D','E'},{'H','P','Q'}};
	  { 
     for(int i=0;i<=2;i++)
        {
    	 for(int j=0;j<=i;j++)
         
         System.out.print(C[i][j]+ " ");
     
          System.out.println();
		
	  }
	}
	}
}
